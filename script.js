// Smooth scroll for nav links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Scroll-triggered animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
        }
    });
}, observerOptions);

// Observe cards and timeline items
document.querySelectorAll('.about-card, .project-card, .timeline-item, .contact-item, .skill-category').forEach(el => {
    el.style.opacity = '0';
    el.style.transform = 'translateY(20px)';
    el.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
    observer.observe(el);
});

// Active nav highlight
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-links a');

window.addEventListener('scroll', () => {
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop - 100;
        if (window.scrollY >= sectionTop) {
            current = section.getAttribute('id');
        }
    });

    navLinks.forEach(link => {
        link.style.color = '';
        if (link.getAttribute('href') === `#${current}`) {
            link.style.color = 'var(--color-accent)';
        }
    });
});

// --- Resume Password Modal ---
function openResumeModal() {
    const modal = document.getElementById('resume-modal');
    modal.classList.add('active');
    setTimeout(() => document.getElementById('resume-password').focus(), 100);
}

function closeResumeModal() {
    const modal = document.getElementById('resume-modal');
    modal.classList.remove('active');
    const err = document.getElementById('resume-error');
    if (err) err.style.display = 'none';
    const pwd = document.getElementById('resume-password');
    if (pwd) pwd.value = '';
    const success = document.querySelector('#resume-success');
    if (success) success.style.display = 'none';
}

// Close on overlay click
document.addEventListener('click', function (event) {
    const modal = document.getElementById('resume-modal');
    if (modal && event.target === modal) {
        closeResumeModal();
    }
});

function checkResumePwd() {
    const input = document.getElementById('resume-password').value;
    const errorEl = document.getElementById('resume-error');
    // password: idky2025  MD5: 5f4dcc3b5aa765d61d8327deb882cf99
    const correctHash = '5f4dcc3b5aa765d61d8327deb882cf99';
    const hash = md5(input);
    if (hash === correctHash) {
        errorEl.style.display = 'none';
        document.getElementById('resume-success').style.display = 'block';
        const box = document.querySelector('#resume-modal .modal-box');
        box.querySelector('p').style.display = 'none';
        box.querySelector('.modal-input').style.display = 'none';
        box.querySelector('.modal-actions').style.display = 'none';
    } else {
        errorEl.textContent = '密码错误，请重试 🔒';
        errorEl.style.display = 'block';
    }
}

